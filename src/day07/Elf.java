package day07;

public class Elf {

	private int count;
	private int step;

	public Elf() {
		setCount(-1);
		setStep(0);
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getStep() {
		return step;
	}

	public void setStep(int step) {
		this.step = step;
	}
}
